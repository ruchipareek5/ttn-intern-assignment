package TrySpring;

public class Student {

    String name;
    int age;
    Dept dept;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Dept getDept() {
        dept.dept();
        return dept;
    }

    public void setDept(Dept dept) {
        this.dept = dept;
    }
}
